import os


# // *---------------------------------------------------------------------* //
def clear():
    """Clears the Python console - Use either clear() or cls()"""
    os.system('cls')
    return None
cls = clear
# // *---------------------------------------------------------------------* //


# // *---------------------------------------------------------------------* //
def clear_all():
    """Clears all variables from the workspace"""
    clear()
    gl = globals().copy()
    for var in gl:
        if var[0] == '_':
            continue
        if 'func' in str(globals()[var]):
            continue
        if 'module' in str(globals()[var]):
            continue

        del globals()[var]
# // *---------------------------------------------------------------------* //

clear_all()
